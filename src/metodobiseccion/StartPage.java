    private void SetImageLabel(JLabel labelName, String url) {
        // crear la imagen del icono
        this.image = new ImageIcon(getClass().getResource(url));

        //redimensionar el icono
        this.icon = new ImageIcon(
                this.image.getImage().getScaledInstance(labelName.getWidth(), labelName.getHeight(), Image.SCALE_SMOOTH)
        );
        
        //asignar el icono
        labelName.setIcon(this.icon);
        this.repaint();
    }


